package model;

public abstract class Prodotto {
    private double peso;
    private double cost;

    public Prodotto() {}

    public Prodotto(double product, double cost){
        this.  peso = peso;
        this.  cost = cost;
    }

    public double getPeso(){
        return  this. peso;
    }

    public double getCosto(){
        return  this. cost;
    }

    public abstract String getAttribute();

    public int getWeight() {
        return getWeight();
    }

    public double getCost() {
        return getCost();
    }
}
